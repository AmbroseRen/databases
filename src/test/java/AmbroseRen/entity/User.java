package AmbroseRen.entity;

public class User {
	private int id;
	private String uname;
	private String name;
	private String pwd;
	private String sex;
	
	public User() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	@Override
	public String toString() {
		return "regist [id=" + id + ", uname=" + uname + ", name=" + name + ", pwd=" + pwd + ", sex=" + sex + "]";
	}
	
	
}
